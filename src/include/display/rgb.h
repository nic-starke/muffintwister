/**
 * @brief A 15-bit colour-space RGB data structure.
 * This allows 32 x 32 x 32 colours.
 */
typedef union {
  struct {
    uint16_t red   : 5;
    uint16_t green : 5;
    uint16_t blue  : 5;
  };
  uint16_t value;
} rgb_15_bit_t;

